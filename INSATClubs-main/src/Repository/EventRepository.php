    public function searchByKeyword($search){
        $events=$this->findAll();
        foreach ($events as $x => $y)
        {
            $t=$y->getTitle();
            $c=$y->getCategory();
            if(stripos($t,$search)=== false && stripos($c,$search)===false)
                unset($events[$x]);
        }
        return $events;
    }

